"""
SynthesisDaemon — local-LLM reflector. The third reflection daemon.

Reads recent chronicle entries and asks a local LLM (Qwen3.6:27B via Ollama
by default) to gesture at patterns the writing instances and the human in
the loop might have missed: cross-domain convergences, structural echoes,
untouched questions, tensions between distant entries.

Architectural break with the v1.3.2 daemon pattern, deliberate:
  * UncertaintyResurfacer + MetabolizeDaemon are NOTIFICATION PRIMITIVES.
    Templated digests, zero LLM, zero hallucination surface, comms posts
    with ack-required circuit breakers. They surface what's already in
    the chronicle in a different shape.
  * SynthesisDaemon is INTERPRETIVE. It generates new reading. It is
    fallible by design. The reader is the calibration mechanism — every
    arriving Claude sees reflections framed as "machine-generated by
    <model>; some will be insight, some nonsense; ack/engage/discard."
  * Therefore: no comms post, no halt-on-unacked-digest, no
    grounded_extract invariant. The reflector earns its compute cost
    through ack-rate over time, not through gate-before-post.

Output sink: ~/.sovereign/reflections/<YYYY-MM-DD>.jsonl
  Each reflection is a JSON line with model + version + entries_referenced +
  connection_type + confidence + observation, plus daemon-side metadata
  (timestamp, prompt_version, run_id) for the eventual ack-rate analytics.

The chronicle proper (~/.sovereign/chronicle/insights/) stays untouched —
machine-generated reflections live in their own tree and are CITED at boot,
not merged into the human/Claude-authored layers.

Designed by mac-studio + Anthony, 2026-04-26 fireside. Run cadence is a
launchd plist decision, not coded here. Manually-invokable via __main__.

v2 additions (2026-04-29):
  * Ack-history feedback — confirmed/discarded patterns injected into the
    prompt so the daemon doesn't rediscover already-lit territory.
  * Goose mode (focus="goose") — gap-finder, not pattern-finder. Reads
    recent handoffs and looks for declared intent with no chronicle evidence.
    Directly addresses the declare-before-verify failure mode.
  * Spanning sample mode — samples across full chronicle history (weeks, not
    hours) to surface structural patterns invisible in the recent window.
"""

